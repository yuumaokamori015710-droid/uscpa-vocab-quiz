const buildExplanation = (seed: FarQuestionSeed) => {
  const [correct, ...others] = seed.choices;
  const optionReview = seed.choices
    .map((choice) => {
      if (choice === seed.correctAnswer) return `${choice}: 正解。${seed.keyTakeaway}`;
      return `${choice}: 不正解。この設問では${seed.trapExplanation}`;
    })
    .join(" / ");

  return [
    `1. 問題のポイント: ${seed.concept}を識別する問題です。`,
    `2. 正解理由: ${seed.correctAnswer}が正解です。${seed.keyTakeaway}`,
    `3. 各選択肢の検討: ${optionReview}`,
    `4. 試験テクニック: ${seed.examTechnique}`,
    `5. ひっかけポイント: ${seed.trapExplanation}`,
    `6. 一言まとめ: ${seed.keyTakeaway}`
  ].join("\n");
};

const q = (seed: FarQuestionSeed): Question => ({
  ...seed,
  subject: "FAR",
  explanationJa: buildExplanation(seed)
});

